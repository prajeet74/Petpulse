    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        jTabbedPane1.setSelectedIndex(2);
        int currentRow = tblPetpulse.getSelectedRow();

        String id = tblPetpulse.getValueAt(currentRow, 0).toString();

        tfSnumber_Update.setText(tblPetpulse.getValueAt(currentRow, 0).toString());

        tfBreed_Update.setText(tblPetpulse.getValueAt(currentRow, 1).toString());

        tfOrigin_Update.setText(tblPetpulse.getValueAt(currentRow, 2).toString());

        String fur = tblPetpulse.getValueAt(currentRow, 3).toString();
        String[] furarr = fur.split(",");
        Component[] components = jPanel9.getComponents();
        for (String furr : furarr) {
            for (Component component : components) {
                if (component instanceof JCheckBox) {
                    JCheckBox checkbox = (JCheckBox) component;
                    if (checkbox.getText().equals(furr)) {
                        checkbox.setSelected(true);
                    }
                }
            }
        }

        String[] values = (tblPetpulse.getValueAt(currentRow, 4).toString()).split("-");
        int minheight_Update = Integer.parseInt(values[0].trim());
        int maxheight_Update = Integer.parseInt(values[1].trim());
        spinnerHeight1_Update.setValue(minheight_Update);
        spinnerHeight2_Update.setValue(maxheight_Update);

        comboBox_Update.setSelectedItem(tblPetpulse.getValueAt(currentRow, 5).toString());

        String[] values1 = (tblPetpulse.getValueAt(currentRow, 6).toString()).split("-");
        int minlongevity_Update = Integer.parseInt(values1[0].trim());
        int maxlongevity_Update = Integer.parseInt(values1[1].trim());
        spinnerLongevity1_Update.setValue(minlongevity_Update);
        spinnerLongevity2_Update.setValue(maxlongevity_Update);

        tfTraits_Update.setText(tblPetpulse.getValueAt(currentRow, 7).toString());

        tfHealthProblem_Update.setText(tblPetpulse.getValueAt(currentRow, 8).toString());

        spinnerObey_Update.setValue(Integer.parseInt(tblPetpulse.getValueAt(currentRow, 9).toString()));

    }//GEN-LAST:event_btnUpdateActionPerformed

