    private void btnAdd4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd4ActionPerformed
       try{
             snumber = Integer.parseInt(tfSnumber.getText());
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(jpanelAdd2, StringUtils.INTEGER_MESSAGE);
       }
       
       if ((tfBreed.getText()).matches("^[a-zA-Z\\s]+$")){
       String breed = tfBreed.getText();
       }else{
          JOptionPane.showMessageDialog(jpanelAdd2, StringUtils.STRING_MESSAGE);
       }
        
       
        String origin = tfOrigin.getText(); 
        
        ArrayList<String> furColors = new ArrayList<>();
        Component[] components = jpanelAdd2.getComponents();
        for (Component component : components) {
            if(component instanceof JCheckBox) {
                JCheckBox checkbox = (JCheckBox) component;
                if (checkbox.isSelected()) {
                    furColors.add(checkbox.getText());
                }
            }
        }
        String checkedFurColors = String.join(",", furColors);
        int minHeight = Integer.parseInt(spinnerHeight1.getValue().toString());
        int maxHeight = Integer.parseInt(spinnerHeight2.getValue().toString());
        String height = minHeight + "-" + maxHeight;
        String eyeColor = comboBox_Add.getSelectedItem().toString();
        int minLongevity = Integer.parseInt(spinnerLongevity2.getValue().toString());
        int maxLongevity = Integer.parseInt(spinnerLongevity2.getValue().toString());
        String longevity = minLongevity + "-" + maxLongevity;
        String characterTraits = tfTraits.getText();
        String healthProblem = tfHealthProblem.getText();
        int obey = Integer.parseInt(spinnerObey.getValue().toString());

        DefaultTableModel tableModel = (DefaultTableModel) tblPetpulse.getModel();
        tableModel.addRow(new Object[]{snumber, breed, origin, checkedFurColors, height, eyeColor, longevity, characterTraits, healthProblem, obey});
    }//GEN-LAST:event_btnAdd4ActionPerformed

